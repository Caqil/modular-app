import { type ClassValue, clsx } from 'clsx';
import { twMerge } from 'tailwind-merge';

/**
 * Utility function to merge Tailwind CSS classes
 * Combines clsx for conditional classes with tailwind-merge for proper Tailwind class merging
 */
export function cn(...inputs: ClassValue[]) {
  return twMerge(clsx(inputs));
}

/**
 * Conditional class name utility
 */
export function conditionalClass(condition: boolean, trueClass: string, falseClass?: string) {
  return condition ? trueClass : falseClass || '';
}

/**
 * Variant class utility for consistent variant handling
 */
export function variantClass(
  base: string,
  variants: Record<string, string>,
  selectedVariant: string
) {
  return cn(base, variants[selectedVariant] || variants.default);
}

